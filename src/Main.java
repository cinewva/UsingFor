//just printing something in console using for
public class Main {
    public static void main(String[] args) {
        // greeting
        System.out.println("Hello and welcome!");

        // using for 5 times
        for (int i = 1; i <= 5; i++) {

            // adding 1 to i and print in console every itteration
            System.out.println("i = " + (i + 1) );
        }
    }
}